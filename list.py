# create a list using []
a = [1, 2, 3, 4, 5, 6]
# print the list using print() function
print(a)

# Access using index using a[0],a[1],a[2]
print(a[1])
# change the value of list using
a[0] = 98
print(a)

# We can create a list using items of different types

f = [24, "yash", False, 8.9]
print(f)
print(type(f[2]))

# List slicing
friends = ["yash", "neeraj", "abhay", "digesh", 45]
